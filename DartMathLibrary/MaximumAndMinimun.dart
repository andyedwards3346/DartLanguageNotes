import 'dart:math';

// Maximum and minimum
// The Math library provides max() and min() methods:

void maximumAndMinimum() {
  print(max(1, 1000)); // 1000
  print(min(1, -1000)); // -1000
}

void main() {
  maximumAndMinimum(); 
}